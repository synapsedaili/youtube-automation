# YouTube API izinleri
SCOPES = ["https://www.googleapis.com/auth/youtube.upload"]

def authenticate_youtube():
    """OAuth2 token'Ä± yÃ¶netir (GitHub Actions uyumlu)."""
    creds = None
    token_path = "token.pickle"

    # Ã–nceden kaydedilmiÅŸ token varsa yÃ¼kle
    if os.path.exists(token_path):
        with open(token_path, "rb") as token:
            creds = pickle.load(token)

    # GeÃ§ersizse yenile veya yeni oluÅŸtur
    if not creds or not creds.valid:
        if creds and creds.expired and creds.refresh_token:
            creds.refresh(Request())
        else:
            # client_secret.json GitHub Secrets'ten decode edilmiÅŸ olmalÄ±
            if not os.path.exists("client_secret.json"):
                raise FileNotFoundError("âŒ client_secret.json bulunamadÄ± (GitHub Secrets'ten decode edilmemiÅŸ)")
            flow = InstalledAppFlow.from_client_secrets_file("client_secret.json", SCOPES)
            # GitHub Actions'ta headless modda Ã§alÄ±ÅŸÄ±r
            creds = flow.run_local_server(port=0, open_browser=False)
        # Token'Ä± kaydet
        with open(token_path, "wb") as token:
            pickle.dump(creds, token)

    return build("youtube", "v3", credentials=creds)

